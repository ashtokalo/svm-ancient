 ; Каноническая программа hello.asm
 ; она выводит строку Hello world! на терминал - текстовый экран

 ; объявляем стек на 512 байт
 stack 512

 ; Подключаем модуль для работы с терминалом
 include terminal.inc
 
 ; message - переменная содержащая выводимую нами строку 
 message db 'Hello world!', 10, 13, 0  ; 0 - конец строки

 ; начало программы 
 start:

  ; создаем поток для работы с терминалом, его номер хранится в переменной 
  ; TrmStream как двойное слово
  call CreatTrmStream
 
  ; помещаяем адрес переменной, которая содержит выводимую строку, в регистр lep
  mov lep, message

  ; вызываем процедуру вывода строки на терминал
  call WriteString

 ; конец программы
 finish: